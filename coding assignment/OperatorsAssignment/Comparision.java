class Comparision{
public static void main(String[] args){
int a=10;
int b=20;
System.out.println("Equal to result : "+(a==b));
System.out.println("NotEqual to result : "+(a!=b));
System.out.println("GreaterThan result : "+(a>b));
System.out.println("LesserThan result : "+(a<b));
System.out.println("GreaterThan Equal to result : "+(a>=b));
System.out.println("LesserThan Equal to result : "+(a<=b));
}
}